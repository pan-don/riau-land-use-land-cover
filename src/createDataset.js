// ─────────────────────────────────────────────
// 3. SENTINEL-2 — MASKING & INDEKS
// ─────────────────────────────────────────────

/**
 * Masking awan dan bayangan menggunakan QA60 + SCL,
 * lalu pilih band optik dan normalisasi reflektansi.
 */
function maskS2(img) {
  var qa  = img.select('QA60');
  var scl = img.select('SCL');

  var qaMask = qa.bitwiseAnd(1 << 10).eq(0)
                 .and(qa.bitwiseAnd(1 << 11).eq(0));

  // SCL: 3=shadow, 8=medium cloud, 9=high cloud, 10=thin cirrus, 11=snow
  var sclMask = scl.neq(3).and(scl.neq(8)).and(scl.neq(9))
                          .and(scl.neq(10)).and(scl.neq(11));

  return img
    .updateMask(qaMask.and(sclMask))
    .select(
      ['B2',   'B3',    'B4',  'B8',  'B11',   'B12'],
      ['blue', 'green', 'red', 'nir', 'swir1', 'swir2']
    )
    .divide(10000);
}

/**
 * Tambahkan indeks spektral: NDVI, NDWI, MNDWI, NDTI, NDBI.
 */
function addS2Indices(img) {
  return img.addBands([
    img.normalizedDifference(['nir',   'red'  ]).rename('NDVI'),
    img.normalizedDifference(['green', 'nir'  ]).rename('NDWI'),
    img.normalizedDifference(['green', 'swir1']).rename('MNDWI'),
    img.normalizedDifference(['swir1', 'swir2']).rename('NDTI'),
    img.normalizedDifference(['swir1', 'nir'  ]).rename('NDBI')
  ]);
}

/**
 * Buat komposit median S2 tahunan untuk wilayah tertentu.
 * FIX: gunakan .map(addS2Indices).median() — bukan .median().call(...)
 * @return {ee.Image} Komposit S2 dengan 11 band (6 optik + 5 indeks)
 */
function buildS2Composite(year, region) {
  var start = ee.Date.fromYMD(year, 1, 1);
  var end   = start.advance(1, 'year');

  return ee.ImageCollection('COPERNICUS/S2_SR_HARMONIZED')
    .filterBounds(region)
    .filterDate(start, end)
    .map(maskS2)
    .map(addS2Indices)   // ← FIX: pindah ke sebelum .median()
    .median();
}


// ─────────────────────────────────────────────
// 4. SENTINEL-1 — FILTER & TEKSTUR
// ─────────────────────────────────────────────

/**
 * Tambahkan fitur tekstur GLCM (contrast, entropy, correlation)
 * untuk polarisasi VV dan VH.
 */
function addS1Texture(img) {
  var vvQ = img.select('VV').multiply(100).toInt16();
  var vhQ = img.select('VH').multiply(100).toInt16();

  var vvTex = vvQ.glcmTexture(1).select(['VV_contrast', 'VV_ent', 'VV_corr']);
  var vhTex = vhQ.glcmTexture(1).select(['VH_contrast', 'VH_ent', 'VH_corr']);

  return img.addBands([vvTex, vhTex]);
}

/**
 * Buat komposit median S1 tahunan (IW, orbit sesuai CONFIG).
 * FIX: sama — .map(addS1Texture).median() bukan .median().call(...)
 * @return {ee.Image} Komposit S1 dengan 8 band (2 SAR + 6 tekstur)
 */
function buildS1Composite(year, region) {
  var start = ee.Date.fromYMD(year, 1, 1);
  var end   = start.advance(1, 'year');

  return ee.ImageCollection('COPERNICUS/S1_GRD')
    .filterBounds(region)
    .filterDate(start, end)
    .filter(ee.Filter.listContains('transmitterReceiverPolarisation', 'VV'))
    .filter(ee.Filter.listContains('transmitterReceiverPolarisation', 'VH'))
    .filter(ee.Filter.eq('instrumentMode', 'IW'))
    .filter(ee.Filter.eq('orbitProperties_pass', CONFIG.orbitPass))
    .map(function(img) { return img.select(['VV', 'VH']); })
    .map(addS1Texture)   // ← FIX: pindah ke sebelum .median()
    .median();
}


// ─────────────────────────────────────────────
// 5. GLO30 DEM — ELEVASI & TERRAIN
// ─────────────────────────────────────────────

/**
 * Buat layer DEM dari Copernicus GLO-30,
 * tambahkan slope dan aspect dari ee.Terrain.
 * @return {ee.Image} 3 band: dem, slope, aspect
 */
function buildDEMComposite(region) {
  var collection = ee.ImageCollection('COPERNICUS/DEM/GLO30')
    .filterBounds(region);

  var nativeProj = collection.first().projection();

  var dem = collection
    .select('DEM')
    .mosaic()
    .setDefaultProjection(nativeProj)
    .rename('dem');

  var terrain = ee.Terrain.products(dem);

  return dem
    .addBands(terrain.select('slope'))
    .addBands(terrain.select('aspect'));
}


// ─────────────────────────────────────────────
// 6. FEATURE STACK — GABUNGAN S2 + S1 + DEM
// ─────────────────────────────────────────────

/**
 * Gabungkan S2, S1, dan GLO30 menjadi satu feature stack.
 * Semua band di-reproject ke skala 30 m (CRS EPSG:4326).
 * Total band: 11 (S2) + 8 (S1) + 3 (DEM) = 22 band
 * @return {ee.Image} Feature stack siap sampling (22 band)
 */
function buildFeatureStack(year, region) {
  var proj30 = ee.Projection('EPSG:4326').atScale(CONFIG.scale);

  var s2 = buildS2Composite(year, region)
             .reproject(proj30);

  var s1 = buildS1Composite(year, region)
             .reproject(proj30);

  var dem = buildDEMComposite(region)
              .reproject(proj30);

  var stack = s2.addBands(s1).addBands(dem).clip(region);

  // Masking pixel yang tidak valid di band manapun
  var validMask = stack.mask().reduce(ee.Reducer.min());
  return stack.updateMask(validMask);
}


// ─────────────────────────────────────────────
// 7. LABEL IMAGE — RASTERISASI ROI
// ─────────────────────────────────────────────

/**
 * Rasterisasi CLASS_FC menjadi label image integer.
 * Pixel di luar semua ROI akan ter-mask.
 * @return {ee.Image} Label image band 'class' (Int16)
 */
function buildLabelImage(region) {
  var painted = ee.Image(-1).toInt16().paint(CLASS_FC, 'class');
  return painted
    .rename('class')
    .toInt16()
    .updateMask(painted.neq(-1))
    .clip(region);
}


// ─────────────────────────────────────────────
// 8. SAMPLING STRATIFIKASI
// ─────────────────────────────────────────────

/**
 * Lakukan stratified sampling dari feature stack + label image.
 * Hanya simpan sampel yang tidak mengandung null pada predictor manapun.
 * @return {ee.FeatureCollection} Sampel bersih dengan predictor + label
 */
function buildSamples(featureStack, labelImage, region) {
  var predictors  = featureStack.bandNames();
  var sampleImage = featureStack.addBands(labelImage);

  var rawSamples = sampleImage.stratifiedSample({
    numPoints:   2000,
    classBand:   'class',
    classValues: CONFIG.classes.values,
    classPoints: CONFIG.classes.points,
    region:      region,
    scale:       CONFIG.scale,
    seed:        CONFIG.seed,
    geometries:  true,
    tileScale:   4,
    dropNulls:   true
  });

  return rawSamples.filter(
    ee.Filter.notNull(predictors.add('class'))
  );
}


